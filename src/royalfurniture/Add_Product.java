    public void fill_combo()
    {
        try {
            conn=DBConnect.connect();
            ps=conn.prepareStatement("select * from category");
            rs=ps.executeQuery();
            while(rs.next())
            {
                cmb_cat.addItem(rs.getString("category_name"));
            }
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

