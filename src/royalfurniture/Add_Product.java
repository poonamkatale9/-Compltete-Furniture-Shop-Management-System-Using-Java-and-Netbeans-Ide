    private void bttn_addproductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttn_addproductActionPerformed
        // TODO add your handling code here:
        if(txt_color.getText().equals("") || txt_pname.getText().equals("")||txt_price.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please fill all the fields");
        }
        else
        {
        try
        {
           conn=DBConnect.connect();
           ps=conn.prepareStatement("insert into product(category,name,price,color) values(?,?,?,?)");
           String s="";
           s+=cmb_cat.getSelectedItem();
           ps.setString(1,s);
           ps.setString(2,txt_pname.getText());
           ps.setString(3, txt_price.getText());
           ps.setString(4, txt_color.getText());
          
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null,"Product added successfully");
          clear_all();
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_bttn_addproductActionPerformed
    }
